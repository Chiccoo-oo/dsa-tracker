// Flatten all problems for stats
export const getAllProblems = () => {
  const all = [];
  DSA_PLAN.forEach(month => {
    month.weeks.forEach(week => {
      week.days.forEach(day => {
        day.problems.forEach(p => {
          if (!all.find(x => x.id === p.id && x.dayRef === day.day)) {
            all.push({ ...p, dayRef: day.day, weekRef: week.week, monthRef: month.month });
          }
        });
      });
    });
  });
  return all;
};

export const getTotalStats = (progress) => {
  const allDays = [];
  DSA_PLAN.forEach(month => {
    month.weeks.forEach(week => {
      week.days.forEach(day => {
        allDays.push(day);
      });
    });
  });

  let totalProblems = 0;
  let solvedProblems = 0;

  allDays.forEach(day => {
    day.problems.forEach(p => {
      const key = `${day.day}-${p.id}`;
      totalProblems++;
      if (progress[key]) solvedProblems++;
    });
  });

  return { totalProblems, solvedProblems, totalDays: allDays.length };
};
