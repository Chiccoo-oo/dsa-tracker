export const getAllDays = () => {
  const days = [];
  DSA_PLAN.forEach(month => {
    month.weeks.forEach(week => {
      week.days.forEach(day => {
        days.push({ ...day, monthTitle: month.monthTitle, weekTitle: week.weekTitle });
      });
    });
  });
  return days;
};

export const getTotalStats = (progress) => {
  const allDays = getAllDays();
  let totalProblems = 0, solvedProblems = 0;
  allDays.forEach(day => {
    day.problems.forEach(prob => {
      const key = `${day.day}-${prob.id}`;
      totalProblems++;
      if (progress[key]) solvedProblems++;
    });
  });
  return { totalProblems, solvedProblems, totalDays: allDays.length };
};